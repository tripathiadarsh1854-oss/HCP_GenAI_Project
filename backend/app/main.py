# --- SCHEMAS ---

class ChatRequest(BaseModel):
    message: str
    thread_id: str = "default_session" # Used by LangGraph MemorySaver to remember the user

# --- STREAMING ENDPOINT ---

@app.post("/api/agent/stream")
async def chat_stream(request: ChatRequest):
    """
    Receives a message from the React UI and streams back the LangGraph agent's 
    thought process, tool executions, and final text response using Server-Sent Events (SSE).
    """
    if not request.message:
        raise HTTPException(status_code=400, detail="Message cannot be empty.")

    async def event_generator():
        try:
            # 1. Format the user's input for LangGraph
            input_state = {"messages": [HumanMessage(content=request.message)]}
            
            # 2. Configure the thread ID so the agent remembers conversation history
            config = {"configurable": {"thread_id": request.thread_id}}

            # 3. Stream the execution graph asynchronously
            # .astream() yields chunks of state as the graph moves from node to node
            async for event in crm_agent.astream(input_state, config=config, stream_mode="updates"):
                
                # event is a dictionary containing the node name and its output
                # Example: {'agent': {'messages': [...]}} or {'tools': {'messages': [...]}}
                for node_name, state_update in event.items():
                    
                    # We extract the latest message generated by this node
                    latest_message = state_update.get("messages", [])[-1]
                    
                    # Package the data to send to the React frontend
                    payload = {
                        "node": node_name,
                        "type": latest_message.type,
                        "content": latest_message.content,
                    }
                    
                    # If the agent decided to call a tool, we stream that intent
                    if hasattr(latest_message, 'tool_calls') and latest_message.tool_calls:
                        payload["tool_calls"] = latest_message.tool_calls

                    # SSE Format requires data to be prefixed with 'data: ' and end with '\n\n'
                    yield f"data: {json.dumps(payload)}\n\n"
                    
                    # Small sleep to yield control back to the event loop
                    await asyncio.sleep(0.01)

            # 4. Send a distinct termination event so the frontend knows to stop loading animations
            yield "data: {\"status\": \"[DONE]\"}\n\n"

        except Exception as e:
            error_payload = {"error": str(e)}
            yield f"data: {json.dumps(error_payload)}\n\n"

    # Return the generator wrapped in FastAPI's StreamingResponse
    return StreamingResponse(event_generator(), media_type="text/event-stream")


