# --- FRONTEND DATA ENDPOINT ---

@app.get("/api/hcps/{hcp_id}")
def get_hcp_profile(hcp_id: int, db: Session = Depends(get_db)):
    """Fetches real-time HCP data and latest sentiment for the React frontend."""
    
    # 1. Query the database for the doctor
    hcp = db.query(HCP).filter(HCP.id == hcp_id).first()
    if not hcp:
        raise HTTPException(status_code=404, detail="HCP not found")
        
    # 2. Get their recent interactions to find the latest sentiment
    interactions = db.query(InteractionLog).filter(
        InteractionLog.hcp_id == hcp_id
    ).order_by(InteractionLog.interaction_date.desc()).all()
    
    # 3. Return the real data
    return {
        "id": hcp.id,
        "name": hcp.name,
        "specialty": hcp.specialty,
        "sentiment": interactions[0].sentiment if interactions else "Neutral",
        "recent_interactions": interactions
    }